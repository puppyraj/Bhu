package Day5;

public class CheckLadder {
	public static void main(String[]args) {
		int num = 0;
		if(num>0) {
			System.out.println(num+" is positive");
		}
		else if(num<0) {
			System.out.println(num+" is negative");
		}
		else if(num==0) {
			System.out.println(num+" is neutral");
		}
		else {
			System.out.println(num+" is invalid");
		
		}
	}

}

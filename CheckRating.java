package Day5;

public class CheckRating {
	public static void main(String[]args) {
		int rating = 3;
		if(rating==5) {
			System.out.println("Excellent");
		}
		else if(rating==4) {
			System.out.println("Good");
		}
		else if(rating==3) {
			System.out.print("Average");
		}
		else if(rating==2) {
			System.out.println("Okay");
		}
		else {
			System.out.println("poor");
			
					
		}
	}
}
